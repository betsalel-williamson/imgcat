
<h1>We the users...</h1>
<h2>ImgCat is a meme-sharing community focused on users</h2>

<p>Over the years, the internet has gotten prickly: Social media pushes toxicity, corporations strip-mine our personal data, and the sites we grew up with sold out.</p>

<p>Our aim is to build something better, simpler, and less-toxic - built around users and the community we build, together.</p>

<h2>Our principles...</h2>
<ul>
	<li>Data privacy - We apply EU-level privacy to all users</li>
	<li>Simple feeds - We use votes & views, not engagement models</li>
	<li>Content controls - Users decide the content they want to see</li>
	<li>Moderation - Users have powerful tools to shape their community</li>
</ul>

<h2>What makes us different...</h2>
<p>The community. We are building robust controls allowing YOU to control your own feed, see the content you want, and ignore what you don't.</p>

<h2>What's the catch?</h2>
<p>Our interface is designed for sharing content within the community. We are a social-media site, not image-hosting. That focus comes with a lot of additional tools and baggage that you won't see elsewhere. Other sites are better for sharing one-off images used for blogs, forums, etc.</p>